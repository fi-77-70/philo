#include "philo.h"

int	ft_atoi(char *str)
{
	int sign;
	int result;

	sign = 1;
	result = 0;
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			sign = -1;
		str++;
	}
	while(*str)
	{
		result = result * 10 + (*str - 48);
		str++;
	}
	return (result * sign);
}